    // metodo para clicar no butão
    private void btnContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContarActionPerformed
        // TODO add your handling code here:
        
         // Pega os valores dos sliders
    int inicio = jSlider1.getValue();
    int fim = jSlider2.getValue();
    int passo = jSlider3.getValue();

    // Atualiza os rotulos com os valores atuais
    lbLInicio.setText(Integer.toString(inicio));
    lblFim.setText(Integer.toString(fim));
    lblPasso.setText(Integer.toString(passo));

    // Cria um modelo para exibir a contagem na JList
    DefaultListModel<String> lista = new DefaultListModel<>();

    // verifica se a contagem é crescente ou descrecente
    if (inicio < fim) {
        for (int i = inicio; i <= fim; i += passo) {
            lista.addElement(Integer.toString(i));
        }
    } else {
        for (int i = inicio; i >= fim; i -= passo) {
            lista.addElement(Integer.toString(i));
        }
    }

    // exibe a lista no jlist
    listResultado.setModel(lista);

    }//GEN-LAST:event_btnContarActionPerformed

